import { currentTimeDate, formatHour } from '@/utils/format'

const getTimeOfDay = (hour: string) => {
  const target = Number(hour)
  if (target >= 5 && target < 12) {
    return '朝'
  } else if (target >= 12 && target < 18) {
    return '昼'
  } else {
    return '夜'
  }
}

export const getCharacterMessage = () => {
  const timeOfDay = getTimeOfDay(formatHour(currentTimeDate))
  if (timeOfDay === '朝') {
    return morningMessages[Math.floor(Math.random() * morningMessages.length)]
  } else if (timeOfDay === '昼') {
    return middayMessages[Math.floor(Math.random() * middayMessages.length)]
  } else {
    return nightMessages[Math.floor(Math.random() * nightMessages.length)]
  }
}

